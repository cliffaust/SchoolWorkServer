    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) libraryTable.getModel();
        String url = "jdbc:mysql://localhost:3306/library?useSSL=false";
        String user = "root";
        String pass = "";
        int min = 1 ;
        int max = 999_999 ;
        int randomNum = ThreadLocalRandom.current().nextInt( min , max + 1 );
//        String uniqueID = UUID.randomUUID().toString()
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection(url, user, pass);
            PreparedStatement psmnt = conn.prepareStatement("insert into library values(?, ?, ?)");
            psmnt.setString(1, bookTitle.getText());
            psmnt.setString(2, bookAuthor.getText());
            psmnt.setInt(3, randomNum);
            psmnt.executeUpdate();
            model.addRow(new Object[]{randomNum, bookTitle.getText(), bookAuthor.getText()});
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        // TODO add your handling code here:
        
        
        DefaultTableModel model = (DefaultTableModel) libraryTable.getModel();
        String url = "jdbc:mysql://localhost:3306/library?useSSL=false";
        String user = "root";
        String pass = "";
        String query = "update library set `Book Title`=?, author=? where `ID`=?";
        
        try{
            int i = libraryTable.getSelectedRow();
            TableModel tableModel = libraryTable.getModel();
            int instance_id = (int) tableModel.getValueAt(i, 0);
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection(url, user, pass);
            PreparedStatement psmnt = conn.prepareStatement(query);
            psmnt.setString(1, bookTitle.getText());
            psmnt.setString(2, bookAuthor.getText());
            psmnt.setInt(3, instance_id);
            psmnt.executeUpdate();
            
            
            model.setValueAt(tableModel.getValueAt(i, 0), libraryTable.getSelectedRow(), 0);
            model.setValueAt(bookTitle.getText(), libraryTable.getSelectedRow(), 1);
            model.setValueAt(bookAuthor.getText(), libraryTable.getSelectedRow(), 2);
        }catch(Exception e){
            System.out.println(e);
        }
        
    }//GEN-LAST:event_editBtnActionPerformed

